    //method getSpikes - returns the spikes in the map.
    public ArrayList<Rectangle> getSpikes() {
        spikesRects = new ArrayList<Rectangle>();
        for (int i = 0; i < map.length; i++) {
            for (int j = 0; j < map[0].length; j++) {
                if (map[i][j] == 1) {
                    spikesRects.add(new Rectangle(j * 55, i * 55 - 17, 55, 36));
                }
            }
        }
        return spikesRects;
    }

