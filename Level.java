    //Sound method - given by Mr. McKenzie
    private void loadSound(){
        try{
            AudioInputStream audioStream = AudioSystem.getAudioInputStream(new File("Sounds/Splash.wav").getAbsoluteFile());
            sound = AudioSystem.getClip();
            sound.open(audioStream);
            soundLoaded = true;
        } catch(Exception e){
            soundLoaded = false;
        }
    }
    //Sound method - given by Mr. McKenzie
    private void playSound(){
        if(soundLoaded == false){
          return;
        }
        if(sound.isRunning() == true){
            sound.stop();
        }
        sound.setFramePosition(0);
        sound.start();
    }
    
    //Method - draw. Goes through each item in the grid, and draws it differently depending on its number, as this represents the square type.
    public void draw(Graphics g){
        for(int i = 0; i < map.length; i++){
            for(int j = 0; j < map[0].length; j++){
                if(map[i][j] == 1 || map[i][j] == 2){
                    g.drawImage(block, j * 55, i * 55 - 17,55,55, null);
                }
                if(map[i][j] == 3){
                    g.drawImage(water, j * 55, i * 55 - 17,55,55, null);
                }
            }
        }
    }
